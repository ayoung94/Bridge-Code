package com.yedam.bridgecode.chat;

public class ChatVO {

	private String chat_content;
	private String chat_group;
	private String chat_time;
	private String chat_from_id;
	private String chat_id;
	
	
	public String getChat_content() {
		return chat_content;
	}
	public void setChat_content(String chat_content) {
		this.chat_content = chat_content;
	}
	public String getChat_group() {
		return chat_group;
	}
	public void setChat_group(String chat_group) {
		this.chat_group = chat_group;
	}
	public String getChat_time() {
		return chat_time;
	}
	public void setChat_time(String chat_time) {
		this.chat_time = chat_time;
	}
	public String getChat_from_id() {
		return chat_from_id;
	}
	public void setChat_from_id(String chat_from_id) {
		this.chat_from_id = chat_from_id;
	}
	public String getChat_id() {
		return chat_id;
	}
	public void setChat_id(String chat_id) {
		this.chat_id = chat_id;
	}

}
